# 사용자가 현재 위치에 있는 폴더 경로 출력 명령어
pwd

# 현재 위치에서 파일, 폴더 목록 출력
ls

# 파일 자세히 확인 / 숨김 파일까지 확인
ls -l
ls -al

# 파일을 시간 순서로 정렬 [전체파일]
ls -alrt

# 디렉토리 생성
mkdir my_dir

# 디렉토리로 이동
cd my_dir

# 최상단 루트 디렉토리로 이동 / 바로 이전 파일로 이동
cd /
cd ..

# 자동완성 -> tab키 

# 절대 경로 폴더로 이동 [슬래시로 시작]
cd /home/yeji/my_dir

# 상대 경로 폴더로 이동 [상위 폴더로 이동] / .은 현재 폴더를 의미
cd ..


